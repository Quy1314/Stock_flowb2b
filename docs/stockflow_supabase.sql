create or replace function public.host_review_listing(
  p_listing_id uuid,
  p_approve boolean,
  p_reason text default null
)
returns public.listings
language plpgsql
security definer
set search_path = public
as $$
declare
  result public.listings;
begin
  if not public.is_host() then
    raise exception 'Host permission required';
  end if;

  update public.listings
  set
    status = case when p_approve then 'approved'::public.listing_status
                  else 'rejected'::public.listing_status end,
    approved_by = case when p_approve then (select auth.uid()) else null end,
    approved_at = case when p_approve then now() else null end,
