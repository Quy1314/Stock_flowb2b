create or replace function public.seller_respond_purchase_request(
  p_request_id uuid,
  p_accept boolean,
  p_confirmed_quantity numeric default null,
  p_agreed_unit_price numeric default null,
  p_note text default null
)
returns public.purchase_requests
language plpgsql
security definer
set search_path = public
as $$
declare
  req public.purchase_requests;
  available_qty numeric;
  locked_row record;
begin
  select pr as purchase_request, l.available_quantity
  into locked_row
  from public.purchase_requests pr
  join public.listings l on l.id = pr.listing_id
  where pr.id = p_request_id
  for update of pr, l;

  if not found then
    raise exception 'Purchase request not found';
  end if;

  req := locked_row.purchase_request;
  available_qty := locked_row.available_quantity;

  if req.seller_id <> (select auth.uid()) then
    raise exception 'Only the listing seller can respond';
  end if;

  if req.status not in (
    'submitted'::public.purchase_request_status,
    'host_review'::public.purchase_request_status,
    'awaiting_seller_confirmation'::public.purchase_request_status
  ) then
    raise exception 'Purchase request is not awaiting seller response';
  end if;

  if p_accept then
    if p_confirmed_quantity is null or p_confirmed_quantity <= 0 then
      raise exception 'Confirmed quantity must be positive';
    end if;
    if p_confirmed_quantity > req.requested_quantity or p_confirmed_quantity > available_qty then
      raise exception 'Confirmed quantity is invalid';
    end if;
  end if;

  update public.purchase_requests
  set
    status = case when p_accept
                  then 'seller_confirmed'::public.purchase_request_status
                  else 'seller_rejected'::public.purchase_request_status end,
    seller_confirmed_quantity = case when p_accept then p_confirmed_quantity else null end,
    agreed_unit_price = case when p_accept
                             then coalesce(p_agreed_unit_price, req.proposed_unit_price)
                             else null end,
    seller_note = p_note,
    seller_confirmed_at = now()
  where id = p_request_id
  returning * into req;

  if req.host_id is not null then
    perform public.notify_user(
      req.host_id,
      case when p_accept then 'Seller đã xác nhận hàng' else 'Seller từ chối yêu cầu' end,
      coalesce(p_note, 'Đã cập nhật trạng thái yêu cầu mua.'),
      'purchase_request',
      req.id
    );
  else
    perform public.notify_hosts(
      case when p_accept then 'Seller đã xác nhận hàng' else 'Seller từ chối yêu cầu' end,
      coalesce(p_note, 'Đã cập nhật trạng thái yêu cầu mua.'),
      'purchase_request',
      req.id
    );
  end if;

  perform public.notify_user(
    req.buyer_id,
    case when p_accept then 'Seller đã xác nhận hàng' else 'Seller không thể cung cấp hàng' end,
    coalesce(p_note, 'Yêu cầu mua đã được cập nhật.'),
    'purchase_request',
    req.id
  );

  return req;
end;
$$;

create or replace function public.host_add_logistics_quote(
