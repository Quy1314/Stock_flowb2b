create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  requested_role text;
  safe_role public.app_role;
begin
  requested_role := lower(coalesce(new.raw_user_meta_data ->> 'role', 'customer'));

  -- Public signup can only create seller/customer accounts.
  -- Host must be assigned manually by an existing host or SQL/service role.
  safe_role := case
    when requested_role = 'seller' then 'seller'::public.app_role
    else 'customer'::public.app_role
  end;

  insert into public.profiles(id, email, full_name, phone, role)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'full_name', ''),
    new.raw_user_meta_data ->> 'phone',
    safe_role
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_auth_user();

create or replace function public.protect_profile_privileges()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if old.role is distinct from new.role
     and (select auth.uid()) is not null
     and not public.is_host() then
    raise exception 'Only a host can change application roles';
  end if;

  if old.is_active is distinct from new.is_active
     and (select auth.uid()) is not null
     and not public.is_host() then
    raise exception 'Only a host can activate or deactivate accounts';
  end if;

  if old.organization_id is distinct from new.organization_id
     and new.organization_id is not null
     and (select auth.uid()) is not null
     and not public.is_host()
     and not exists (
       select 1
       from public.organizations o
       where o.id = new.organization_id
         and o.created_by = (select auth.uid())
     ) then
    raise exception 'You can only join an organization that you created';
  end if;

  return new;
end;
$$;

drop trigger if exists protect_profile_privileges_trigger on public.profiles;
create trigger protect_profile_privileges_trigger
  before update on public.profiles
  for each row execute function public.protect_profile_privileges();

create or replace function public.enforce_listing_rules()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_host() then
    if tg_op = 'INSERT' then
      new.seller_id := (select auth.uid());
      new.reserved_quantity := 0;
      if new.status not in ('draft'::public.listing_status, 'pending_review'::public.listing_status) then
        new.status := 'pending_review';
      end if;
      new.approved_by := null;
      new.approved_at := null;
      new.rejection_reason := null;
    else
      new.created_at := old.created_at;
      new.reserved_quantity := old.reserved_quantity;

      if new.seller_id <> old.seller_id then
        raise exception 'Seller cannot transfer listing ownership';
      end if;

      if new.status is distinct from old.status
         and new.status in (
           'approved'::public.listing_status,
           'rejected'::public.listing_status,
           'sold_out'::public.listing_status
         ) then
        raise exception 'Only a host can set this listing status';
      end if;

      if old.status = 'approved'::public.listing_status
         and (
           new.product_name is distinct from old.product_name or
           new.description is distinct from old.description or
           new.quantity is distinct from old.quantity or
           new.unit is distinct from old.unit or
           new.unit_price is distinct from old.unit_price or
           new.condition_text is distinct from old.condition_text or
           new.manufacturing_date is distinct from old.manufacturing_date or
           new.expiry_date is distinct from old.expiry_date or
           new.warehouse_id is distinct from old.warehouse_id or
           new.category_id is distinct from old.category_id
         ) then
        new.status := 'pending_review';
        new.approved_by := null;
        new.approved_at := null;
        new.rejection_reason := null;
      else
        new.approved_by := old.approved_by;
        new.approved_at := old.approved_at;
        new.rejection_reason := old.rejection_reason;
      end if;
    end if;
  end if;

  return new;
end;
$$;

drop trigger if exists enforce_listing_rules_trigger on public.listings;
create trigger enforce_listing_rules_trigger
  before insert or update on public.listings
  for each row execute function public.enforce_listing_rules();

create or replace function public.prepare_purchase_request()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_listing public.listings%rowtype;
  buyer_org uuid;
begin
  if public.current_app_role() <> 'customer'::public.app_role then
    raise exception 'Only customer accounts can create purchase requests';
  end if;

  select * into target_listing
  from public.listings
  where id = new.listing_id
  for update;

  if not found or target_listing.status <> 'approved'::public.listing_status then
    raise exception 'Listing is not available for purchase';
  end if;

  if new.requested_quantity > target_listing.available_quantity then
    raise exception 'Requested quantity exceeds available quantity';
  end if;

  select organization_id into buyer_org
  from public.profiles
  where id = (select auth.uid());

  if buyer_org is null then
    raise exception 'Buyer must create and attach an organization first';
  end if;

  if not exists (
    select 1 from public.warehouses w
    where w.id = new.buyer_warehouse_id and w.organization_id = buyer_org
  ) then
    raise exception 'Delivery warehouse does not belong to the buyer organization';
  end if;

  new.buyer_id := (select auth.uid());
  new.buyer_organization_id := buyer_org;
  new.seller_id := target_listing.seller_id;
  new.proposed_unit_price := coalesce(new.proposed_unit_price, target_listing.unit_price);
  new.status := 'submitted';
  new.host_id := null;

  return new;
end;
$$;

drop trigger if exists prepare_purchase_request_trigger on public.purchase_requests;
