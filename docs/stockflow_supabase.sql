create or replace function public.host_create_order(
  p_request_id uuid,
  p_platform_fee numeric default 0
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  req public.purchase_requests;
  listing_row public.listings;
  quote_row public.logistics_quotes;
  seller_warehouse public.warehouses;
  buyer_warehouse public.warehouses;
  seller_org public.organizations;
  partner public.logistics_partners;
  order_row public.orders;
  goods_total numeric(18,2);
begin
  if not public.is_host() then
    raise exception 'Host permission required';
  end if;

  if p_platform_fee < 0 then
    raise exception 'Platform fee cannot be negative';
  end if;

  select * into req
  from public.purchase_requests
  where id = p_request_id
  for update;

  if not found or req.status <> 'buyer_confirmed'::public.purchase_request_status then
    raise exception 'Request is not ready to become an order';
  end if;

  select * into listing_row
  from public.listings
  where id = req.listing_id
  for update;

  if req.seller_confirmed_quantity is null
     or req.seller_confirmed_quantity > listing_row.available_quantity then
    raise exception 'Insufficient available stock';
  end if;

  if req.agreed_unit_price is null then
    raise exception 'Agreed unit price is required';
  end if;

  select * into quote_row
  from public.logistics_quotes
  where id = req.selected_logistics_quote_id
    and purchase_request_id = req.id
    and status = 'accepted';

  if not found then
    raise exception 'Accepted logistics quote not found';
  end if;

  select * into seller_warehouse from public.warehouses where id = listing_row.warehouse_id;
  select * into buyer_warehouse from public.warehouses where id = req.buyer_warehouse_id;
  select * into seller_org from public.organizations where id = listing_row.seller_organization_id;
  select * into partner from public.logistics_partners where id = quote_row.logistics_partner_id;

  goods_total := round(req.seller_confirmed_quantity * req.agreed_unit_price, 2);

  insert into public.orders(
    purchase_request_id, listing_id, seller_id, buyer_id, host_id,
    logistics_quote_id, quantity, unit, unit_price, goods_amount,
    shipping_fee, platform_fee, pickup_address_snapshot,
    delivery_address_snapshot, status
  ) values (
    req.id, listing_row.id, req.seller_id, req.buyer_id, (select auth.uid()),
    quote_row.id, req.seller_confirmed_quantity, listing_row.unit,
    req.agreed_unit_price, goods_total, quote_row.shipping_fee,
    p_platform_fee,
    concat_ws(', ', seller_warehouse.name, seller_warehouse.address,
              seller_warehouse.district, seller_warehouse.city),
    concat_ws(', ', buyer_warehouse.name, buyer_warehouse.address,
              buyer_warehouse.district, buyer_warehouse.city),
    'awaiting_payment'
  ) returning * into order_row;

  update public.listings
  set reserved_quantity = reserved_quantity + order_row.quantity
  where id = listing_row.id;

  insert into public.shipments(
    order_id, logistics_partner_id, status,
    scheduled_pickup_at, note
  ) values (
    order_row.id, quote_row.logistics_partner_id, 'scheduled',
    quote_row.estimated_pickup_at, quote_row.note
  );

  insert into public.payments(order_id, payer_id, payment_type, amount, beneficiary_name)
  values
    (order_row.id, order_row.buyer_id, 'goods', order_row.goods_amount, seller_org.name),
    (order_row.id, order_row.buyer_id, 'shipping', order_row.shipping_fee, partner.name),
    (order_row.id, order_row.buyer_id, 'platform_fee', order_row.platform_fee, 'StockFlow');

  update public.purchase_requests
  set status = 'converted_to_order', host_id = (select auth.uid())
  where id = req.id;

  perform public.notify_user(
    order_row.buyer_id,
    'Đơn hàng đã được tạo',
    'Vui lòng hoàn tất các khoản thanh toán theo đơn ' || order_row.order_number || '.',
    'order', order_row.id
  );

  perform public.notify_user(
    order_row.seller_id,
    'Đơn hàng đã được xác nhận',
    'Vui lòng chuẩn bị hàng để đối tác logistics đến nhận.',
    'order', order_row.id
  );

  return order_row;
end;
$$;

create or replace function public.sync_inventory_after_order_status()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if old.status <> 'cancelled'::public.order_status
     and new.status = 'cancelled'::public.order_status
     and old.status <> 'completed'::public.order_status then
    update public.listings
    set reserved_quantity = greatest(reserved_quantity - new.quantity, 0)
    where id = new.listing_id;
  end if;

  if old.status <> 'completed'::public.order_status
     and new.status = 'completed'::public.order_status then
    update public.listings
    set quantity = greatest(quantity - new.quantity, 0),
        reserved_quantity = greatest(reserved_quantity - new.quantity, 0),
        status = case when greatest(quantity - new.quantity, 0) = 0
                      then 'sold_out'::public.listing_status
                      else status end
    where id = new.listing_id;

    new.completed_at := coalesce(new.completed_at, now());
  end if;

  return new;
end;
$$;

drop trigger if exists sync_inventory_after_order_status_trigger on public.orders;
create trigger sync_inventory_after_order_status_trigger
  before update of status on public.orders
  for each row execute function public.sync_inventory_after_order_status();

create or replace function public.sync_order_from_shipment()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_order public.orders;
begin
  if new.status is distinct from old.status then
    insert into public.shipment_events(shipment_id, status, message, created_by)
    values (
      new.id,
      new.status,
      coalesce(new.note, 'Shipment status updated'),
      (select auth.uid())
    );

    update public.orders
    set status = case new.status
      when 'scheduled'::public.shipment_status then 'pickup_scheduled'::public.order_status
      when 'picked_up'::public.shipment_status then 'picked_up'::public.order_status
      when 'in_transit'::public.shipment_status then 'in_transit'::public.order_status
      when 'delivered'::public.shipment_status then 'delivered'::public.order_status
      when 'cancelled'::public.shipment_status then 'cancelled'::public.order_status
      else status
    end
    where id = new.order_id
    returning * into target_order;

    perform public.notify_user(
      target_order.buyer_id,
      'Cập nhật giao hàng',
      'Trạng thái mới: ' || new.status::text,
      'shipment', new.id
