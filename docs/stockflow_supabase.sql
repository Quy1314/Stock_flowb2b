create or replace function public.buyer_confirm_logistics_quote(
  p_request_id uuid,
  p_quote_id uuid
)
returns public.purchase_requests
language plpgsql
security definer
set search_path = public
as $$
declare
  req public.purchase_requests;
begin
  select * into req
  from public.purchase_requests
  where id = p_request_id
  for update;

  if not found then
    raise exception 'Purchase request not found';
  end if;

  if req.buyer_id <> (select auth.uid()) then
    raise exception 'Only the buyer can confirm this quote';
  end if;

  if req.status <> 'quoted'::public.purchase_request_status then
    raise exception 'Request is not awaiting buyer confirmation';
  end if;

  if not exists (
    select 1 from public.logistics_quotes q
    where q.id = p_quote_id
      and q.purchase_request_id = p_request_id
      and q.status = 'submitted'::public.quote_status
      and (q.valid_until is null or q.valid_until >= now())
  ) then
    raise exception 'Quote is invalid or expired';
  end if;

  update public.logistics_quotes
  set status = case when id = p_quote_id
                    then 'accepted'::public.quote_status
                    else 'rejected'::public.quote_status end
  where purchase_request_id = p_request_id
    and status = 'submitted'::public.quote_status;

  update public.purchase_requests
  set selected_logistics_quote_id = p_quote_id,
      status = 'buyer_confirmed',
      buyer_confirmed_at = now()
  where id = p_request_id
  returning * into req;

  if req.host_id is not null then
    perform public.notify_user(
      req.host_id,
      'Buyer đã xác nhận giao dịch',
      'Có thể tạo đơn hàng và điều phối lấy hàng.',
      'purchase_request',
      req.id
    );
  else
    perform public.notify_hosts(
      'Buyer đã xác nhận giao dịch',
      'Có thể tạo đơn hàng và điều phối lấy hàng.',
      'purchase_request',
      req.id
    );
  end if;

  return req;
end;
